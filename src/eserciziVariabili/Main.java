package eserciziVariabili;

public class Main {

	public static void main(String[] args) {

		/*
		 * Esercizio 1 
		 * Scrivi un programma Java che dichiara una variabile intera
		 * chiamata "numero" e le assegna il valore 10. Stampa il valore della
		 * variabile.
		 */

		int numero = 10;
		System.out.println("Il numero è: " + numero);

		/*
		 * Esercizio 2 
		 * Scrivi un programma Java che dichiara una variabile di tipo float
		 * chiamata "altezza" e le assegna il valore 1.75. Stampa il valore della
		 * variabile.
		 */

		float altezza = 1.75f;
		System.out.println("L'altezza è: " + altezza);

		/*
		 * Esercizio 3 
		 * Scrivi un programma Java che dichiara una variabile di tipo
		 * boolean chiamata "isPieno" e le assegna il valore true. Stampa il valore
		 * della variabile.
		 */

		boolean isPieno = true;
		System.out.println("Il valore è impostato su: " + isPieno);

		/*
		 * Esercizio 4 
		 * Scrivi un programma Java che dichiara una variabile di tipo char
		 * chiamata "lettera" e le assegna il valore 'A'. Stampa il valore della
		 * variabile.
		 */

		char lettera = 'A';
		System.out.println("La lettera è la: " + lettera);

		/*
		 * Esercizio 5 
		 * Scrivi un programma Java che dichiara una variabile di tipo
		 * String chiamata "nome" e le assegna il valore "Mario Rossi". Stampa il valore
		 * della variabile.
		 */

		String nome = "Mario Rossi";
		System.out.println("Il nome è: " + nome);

		/*
		 * Esercizio 6 
		 * Scrivi un programma Java che dichiara una variabile di tipo
		 * double chiamata "peso" e le assegna il valore 68.5. Stampa il valore della
		 * variabile.
		 */

		double peso = 68.5;
		System.out.println("Il peso è di: " + peso);

		/*
		 * Esercizio 7 
		 * Scrivi un programma Java che dichiara due variabili intere
		 * chiamate "numero1" e "numero2" e le assegna i valori 5 e 10 rispettivamente.
		 * Calcola la somma delle due variabili e stampa il risultato.
		 */

		int numero1 = 5;
		int numero2 = 10;
		int somma = numero1 + numero2;
		System.out.println("La somma è: " + somma);

		/*
		 * Esercizio 8 
		 * Scrivi un programma Java che dichiara due variabili di tipo
		 * double chiamate "prezzo1" e "prezzo2" e le assegna i valori 4.99 e 2.5
		 * rispettivamente. Calcola la differenza tra le due variabili e stampa il
		 * risultato.
		 */

		double prezzo1 = 4.99;
		double prezzo2 = 2.5;
		double differenza = prezzo1 - prezzo2;
		System.out.println("La differenza è di: " + differenza);

		/*
		 * Esercizio 9 
		 * Scrivi un programma Java che dichiara una variabile di tipo long
		 * chiamata "popolazioneMondiale" e le assegna il valore 7800000000. Stampa il
		 * valore della variabile.
		 */

		long popolazioneMondiale = 7800000000l;
		System.out.println("La popolazione mondiale è di: " + popolazioneMondiale);

		/*
		 * Esercizio 10 
		 * Scrivi un programma Java che dichiara una variabile di tipo byte
		 * chiamata "eta" e le assegna il valore 25. Stampa il valore della variabile.
		 */

		byte eta = 25;
		System.out.println("L'età è di: " + eta);
	}

}
